#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int num;

	if (n < 0)
	{
		num = -1 * n;
		ft_putchar('-');
	}
	else
		num = n;
	if (num >= 10)
		ft_putnbr(num / 10);
	ft_putchar((num % 10) + '0');
}
